// Custom Test Logic
echo "Starting Test...\n";

try {
    $db =& DB();
    echo "Database Connected.\n";
    
    // 1. Check if 'happy_story' table exists
    if (!$db->table_exists('happy_story')) {
        die("ERROR: Table 'happy_story' does not exist.\n");
    }
    echo "Table 'happy_story' exists.\n";

    // 2. Attempt to Insert a Dummy Story
    $data = array(
        'title' => 'Test Story ' . date('H:i:s'),
        'description' => 'This is a test story generated by the debug script.',
        'date' => date('Y-m-d'),
        'program_date' => date('Y-m-d'),
        'legion_id' => 0, // Simulating Super Admin/No Legion
        'partner_name' => 'Test Partner',
        'program_area' => 'Test Area',
        'legion_name' => 'Test Legion',
        'area_name' => 'Test Location',
        'approval_status' => 1,
        'image' => '',
        'posted_by' => 1 // Assuming 1 is a valid admin ID
    );

    echo "Attempting insert with data: " . json_encode($data) . "\n";
    
    if ($db->insert('happy_story', $data)) {
        echo "SUCCESS: Insert Query Executed.\n";
        $insert_id = $db->insert_id();
        echo "Inserted ID: " . $insert_id . "\n";
        
        // 3. Verify Retrieval
        $query = $db->get_where('happy_story', array('happy_story_id' => $insert_id));
        if ($query->num_rows() > 0) {
            echo "VERIFIED: Record found in database.\n";
            print_r($query->row_array());
        } else {
            echo "ERROR: Insert reported success but record not found.\n";
        }
    } else {
        echo "ERROR: Insert Failed.\n";
        print_r($db->error());
    }

} catch (Exception $e) {
    echo "EXCEPTION: " . $e->getMessage() . "\n";
}
